/**
 * Export a MySQL Workbench–friendly structure-only dump for ERD reverse engineer.
 *
 * Usage: node scripts/export-erd-schema.js
 * Output: artifacts/database/erd-schema-clean.sql
 */
require("dotenv").config();
const fs = require("node:fs/promises");
const path = require("node:path");
const mysql = require("mysql2/promise");

async function main() {
    const connection = await mysql.createConnection({
        host: process.env.DB_HOST,
        port: Number(process.env.DB_PORT || 3306),
        user: process.env.DB_USER,
        password: process.env.DB_PASSWORD || "",
        database: process.env.DB_NAME,
        ssl: process.env.DB_SSL === "false" ? undefined : { rejectUnauthorized: false },
        multipleStatements: false
    });

    try {
        const [[meta]] = await connection.query(
            "SELECT @@version AS version, @@sql_mode AS sql_mode, DATABASE() AS db"
        );
        console.log("Connected:", meta);

        const [tableRows] = await connection.query(
            `SELECT TABLE_NAME AS name
             FROM information_schema.TABLES
             WHERE TABLE_SCHEMA = DATABASE()
               AND TABLE_TYPE = 'BASE TABLE'
             ORDER BY TABLE_NAME`
        );

        const lines = [
            "-- POS Glasses schema for MySQL Workbench EER reverse engineer",
            `-- Source DB: ${meta.db}`,
            `-- MySQL: ${meta.version}`,
            `-- sql_mode: ${meta.sql_mode}`,
            "-- Generated by scripts/export-erd-schema.js",
            "-- Tips: File > Import > Reverse Engineer MySQL Create Script...",
            "-- Do NOT include USE/CREATE DATABASE lines for Workbench reverse engineer.",
            "SET NAMES utf8mb4;",
            "SET FOREIGN_KEY_CHECKS=0;",
            ""
        ];

        for (const row of tableRows) {
            const tableName = row.name;
            const [createRows] = await connection.query(`SHOW CREATE TABLE \`${tableName}\``);
            const createSql = createRows[0]["Create Table"] || createRows[0]["Create Table".toLowerCase()];
            if (!createSql) {
                console.warn("Skip (no CREATE):", tableName);
                continue;
            }

            // Aiven often runs with ANSI_QUOTES, so SHOW CREATE TABLE emits
            // double-quoted identifiers. MySQL Workbench reverse engineer expects
            // classic backticks and fails with "not valid ... expecting an identifier".
            let cleaned = String(createSql);

            // Convert "identifier" -> `identifier` (ANSI_QUOTES style DDL).
            cleaned = cleaned.replace(/"([A-Za-z0-9_]+)"/g, "`$1`");

            // Strip schema-qualified names: CREATE TABLE `defaultdb`.`users`
            cleaned = cleaned
                .replace(/^CREATE TABLE `[A-Za-z0-9_]+`\./i, "CREATE TABLE ")
                .replace(/REFERENCES `[A-Za-z0-9_]+`\./gi, "REFERENCES ");

            // Ensure statement ends with semicolon.
            cleaned = cleaned.trim().replace(/;?\s*$/, ";");

            lines.push(`-- Table: ${tableName}`);
            lines.push(cleaned);
            lines.push("");
        }

        lines.push("SET FOREIGN_KEY_CHECKS=1;");
        lines.push("");

        const outDir = path.join(__dirname, "..", "artifacts", "database");
        await fs.mkdir(outDir, { recursive: true });
        const outPath = path.join(outDir, "erd-schema-clean.sql");
        await fs.writeFile(outPath, lines.join("\n"), "utf8");
        console.log(`Wrote ${tableRows.length} tables -> ${outPath}`);
    } finally {
        await connection.end();
    }
}

main().catch((error) => {
    console.error(error);
    process.exit(1);
});
